// DDrawScreenFront -                                                       ณ
//  This is the displayed or Physical screen.  It may be used with several  ณ
//  back buffers.                                                           ณ
//ฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤู
class DDrawScreenFront
{
public:
    LPDIRECTDRAW            lpDD;       // Direct Draw Object.
    LPDIRECTDRAWSURFACE     lpDDSurf;   // pointer to Display surface.
    HRESULT                 ddrval;     // Holds the return values DD calls.
    HWND                    hwnd;
    WNDCLASS                wc;         
    DDSURFACEDESC           ddsd;       // DDraw Surface description.
    DDSCAPS                 ddscaps;    // DDraw surface capabilities.
        
    DDrawScreenFront()  {};

    BOOL init(int xres,int yres,int bpp);

};


//ฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฟ
// DDrawScreenBack -                                                        ณ
//  This is the back buffer that may be copied to a DDrawScreenFront        ณ
//ฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤฤู
class DDrawScreenBack
{
    LPDIRECTDRAW            lpDD;       // Direct Draw Object.
    LPDIRECTDRAWSURFACE     lpDDSurf;   // pointer to Back buffer's surface.
    HRESULT                 ddrval;     // Holds the return values DD calls.
    HWND                    hwnd;
    WNDCLASS                wc;         
    DDSURFACEDESC           ddsd;       // DDraw Surface description.
    DDSCAPS                 ddscaps;    // DDraw surface capabilities.
};

